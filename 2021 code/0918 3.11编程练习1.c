//通过试验观察系统如何处理整数上溢、浮点数上溢和浮点数下溢的情况
//int类型（整数类型）占用4字节，范围为-2^ 31 ~ 2^31-1,即-2147483648 ~ 2147483647，
//十六进制表示：0x80000000 ~ 0x7FFFFFFF。
//float类型（单精度浮点数类型）占用4字节，数值范围为 -3.4E+38 到 3.4E+38
#include <stdio.h>
int main(void) 
{
    int a = 2147483647;
    float b = 3.4E38 * 100.0f; 
    float c = (3.4e-38) / (100.0E100);
    printf("%d %d %d\n", a, a+1, a+2); //整数上溢
    printf("%e\n", b); //浮点数上溢
    printf("%e", c); //浮点数下溢
    return 0;
}
